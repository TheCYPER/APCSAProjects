package APCSAGuides.Lesson5;

public class Demo04 {
    public static void main(String[] args) {
        int x = 10;
        while( x < 20 ) {    // 只要x还是小于20的
            System.out.print("value of x : " + x );
            x++;              // x就会一直变大
            System.out.print("\n");
        }
    }
}
